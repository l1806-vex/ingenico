#define customize_card_entry_message_on_terminal 1
#define customize_card_entry_amount_on_terminal  1
#define customize_card_entry_target_on_terminal   1
#define customize_card_entry_check_bitmap_on_terminal   1
#define customize_card_entry_tpass_bitmap_on_terminal   1
#define customize_card_entry_pinpad_2_lines       0
#define customize_card_entry_pinpad_graphic       1
#define customize_card_entry_C30_target           1
#define Display_header_on_BW_terminal            1

void ServiceCardEntry(int size, S_CUST_CARD_Entry *infoscust)
{
  FILE *handle;
  unsigned char *amount;
  unsigned char *bitmap;
  unsigned char buf_amount[51];
  unsigned char buf_currency[51];
  unsigned char buf_final[102];  

  if(infoscust->periph==PERIPH_DISPLAY)
  {
     handle = fopen("DISPLAY","w");

     if(customize_card_entry_message_on_terminal)
     {
       if (GetGraphicMode()==PERIPH_DISPLAY_EXTENDED)
       {
         DrawExtendedString(20,41,"MY MESSAGE",OFF,_XLARGE_,_PROP_WIDTH_);
       }
       else
       {
         DrawExtendedString(20,37,"MY MESSAGE",OFF,_XLARGE_,_PROP_WIDTH_);
       }
     }
     else
     {
       infoscust->fct_mess(infoscust->MessWaitCard,infoscust->flag_cless,1);
     }
     if(customize_card_entry_amount_on_terminal)
     {
       amount=(unsigned char *)&infoscust->amount;
       Amount_Format(amount,&infoscust->currency_infos.currency,SYMBOL_EURO,buf_final,buf_amount,buf_currency);
       if (GetGraphicMode()==PERIPH_DISPLAY_EXTENDED)
       {
          DrawExtendedString(20,20,buf_final,OFF,_MEDIUM_,_PROP_WIDTH_);
       }
       else
       {
         DrawExtendedString(20,20,buf_final,OFF,_LARGE_,_PROP_WIDTH_);
       }      
     }
     else
     {
       amount=(unsigned char *)&infoscust->amount;
       if (GetGraphicMode()==PERIPH_DISPLAY_EXTENDED)
       infoscust->fct_amount(amount,&infoscust->currency_infos,infoscust->MessWaitCard,PERIPH_DISPLAY_EXTENDED,infoscust->flag_cless,0);
       else
       {
          if(Display_header_on_BW_terminal)
          infoscust->fct_header((IsHeader()?1:(IsIWL220()?1:0)),infoscust->periph,infoscust->flag_cless,0);
          infoscust->fct_amount(amount,&infoscust->currency_infos,infoscust->MessWaitCard,PERIPH_DISPLAY,infoscust->flag_cless,0);
       }     
     }    
     if(customize_card_entry_check_bitmap_on_terminal)
     {
       if(infoscust->flag_check)
       infoscust->fct_check();
     }
     else
     {
     }
     if(customize_card_entry_tpass_bitmap_on_terminal)
     {
        if(infoscust->flag_tpass)
        {
           if (GetGraphicMode()==PERIPH_DISPLAY_EXTENDED)
           {
             bitmap=(unsigned char *)&MyBitmapTpassColor;
             DrawBmp(95,209,bitmap,FIOCTL_SET_FULL_COLOR);
           }
           else
           {
             bitmap=(unsigned char *)&MyBitmapTpass;
             SetBmp(32,54,bitmap,_OFF_);
           }
        }
     }
     else
     {
       if(infoscust->flag_tpass)
       infoscust->fct_tpass();
     }

     if(customize_card_entry_target_on_terminal)
     {
        if(infoscust->flag_cless)
        {  
          if (GetGraphicMode()==PERIPH_DISPLAY_EXTENDED)
          {
             bitmap=(unsigned char *)&MyBitmapClessColor;
             DrawBmp(AFF_CLESS_X_X07,AFF_CLESS_Y_X07,bitmap,FIOCTL_SET_FULL_COLOR);
          }
          else
          {
             bitmap=(unsigned char *)&MyBitmapCless;
             SetBmp(infoscust->x_target_cless_position,infoscust->y_target_cless_position,bitmap,_OFF_);      
          }
        }     
     }
     else
     {
       if(infoscust->flag_cless)
       infoscust->fct_cless();
     }
     fclose(handle); 
  }
  else if( infoscust->periph==PERIPH_PPR)
  {
     if (PSQ_Est_pinpad() && !PSQ_est_pinpad_graphique())
     {
        if (customize_card_entry_pinpad_2_lines)
        {
          strcpy(buf_final,"");
          amount=(unsigned char *)&infoscust->amount;
          Amount_Format(amount,&infoscust->currency_infos.currency,SYMBOL_EURO,buf_final,buf_amount,buf_currency);
 
          PPS_firstline();
          send_message_pinpad ((char *)buf_final );
        
          PPS_newline();
          send_message_pinpad ( "MY MESSAGE" );
        }
        else
        {
           amount=(unsigned char *)&infoscust->amount;
           infoscust->fct_pinpad2lines(amount,&infoscust->currency_infos.currency,infoscust->MessWaitCard);
        }
     }  
     else
     {
        
        InitContexteGraphique(infoscust->periph);
        _clrscr();
        if (customize_card_entry_pinpad_graphic)
        {
           infoscust->fct_header(0,infoscust->periph,infoscust->flag_cless,0);
           amount=(unsigned char *)&infoscust->amount;
           
           _DrawExtendedString(30,25,"MY MESSAGE",OFF,_MEDIUM_,_PROP_WIDTH_);
             
           Amount_Format(amount,&infoscust->currency_infos.currency,SYMBOL_EURO,buf_final,buf_amount,buf_currency);
           DrawExtendedString(30,15,buf_final,OFF,_SMALL_,_PROP_WIDTH_);

           if(infoscust->flag_cless)
           {
             bitmap=(unsigned char *)&MyBitmapCless;
             SetBmp(infoscust->x_target_cless_position,infoscust->y_target_cless_position,bitmap,_OFF_);      
           }           
        }
        else
        {
           infoscust->fct_header(0,infoscust->periph,infoscust->flag_cless,0);
           amount=(unsigned char *)&infoscust->amount;
           infoscust->fct_amount(amount,&infoscust->currency_infos,infoscust->MessWaitCard,infoscust->periph,infoscust->flag_cless,0);
           infoscust->fct_mess(infoscust->MessWaitCard,infoscust->flag_cless,1);
           if(infoscust->flag_cless)
           infoscust->fct_cless();
        }
           
        InitContexteGraphique(PERIPH_DISPLAY);
     }
     
  }
  else if( infoscust->periph==PERIPH_C30)
  {
    InitContexteGraphique(infoscust->periph);
    _clrscr();
    if (customize_card_entry_C30_target)
    {
      amount=(unsigned char *)&infoscust->amount;
      _DrawExtendedString(30,25,"MY MESSAGE",OFF,_MEDIUM_,_PROP_WIDTH_);
      Amount_Format(amount,&infoscust->currency_infos.currency,SYMBOL_EURO,buf_final,buf_amount,buf_currency);
      DrawExtendedString(30,15,buf_final,OFF,_SMALL_,_PROP_WIDTH_);
    }
    else
    {
      infoscust->fct_header(0,infoscust->periph,infoscust->flag_cless,0);
      amount=(unsigned char *)&infoscust->amount;
      infoscust->fct_amount(amount,&infoscust->currency_infos,infoscust->MessWaitCard,infoscust->periph,infoscust->flag_cless,0);
      infoscust->fct_mess(infoscust->MessWaitCard,infoscust->flag_cless,1);
    }
    
    InitContexteGraphique(PERIPH_DISPLAY);
  }
  
}

